package com.itheima.arithmeticoperator;

public class ArithmeticoperatorDemo5 {
    public static void main(String[] args) {
        //++ 和 --
        int a = 10;
        //表示将变量a的值加1
        a++;
        System.out.println(a);
        //表示将变量a的值加1
        ++a;
        System.out.println(a);
        //表示将变量a的值减1
        a--;
        System.out.println(a);
        //表示将变量a的值减1
        a--;
        System.out.println(a);
    }
}
